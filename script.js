let marcadorUbicacion, circuloPrecision;

mapa.locate({
    watch: true, 
    setView: false, 
    enableHighAccuracy: true, 
    timeout: 15000
});

mapa.on('locationfound', function(e) {
    const radio = e.accuracy / 2;
    if (marcadorUbicacion) {
        marcadorUbicacion.setLatLng(e.latlng);
        circuloPrecision.setLatLng(e.latlng);
        circuloPrecision.setRadius(radio);
    } else {
        circuloPrecision = L.circle(e.latlng, {radius: radio, color: '#007AFF', fillOpacity: 0.15}).addTo(mapa);
        marcadorUbicacion = L.circleMarker(e.latlng, {radius: 8, fillColor: '#007AFF', color: '#fff', weight: 2, fillOpacity: 1}).addTo(mapa);
    }
});

mapa.on('locationerror', function(e) {
    console.warn("Aviso de GPS: " + e.message);
});


